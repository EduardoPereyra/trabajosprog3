package practico4;

public class ObjetoValor {
	private double peso;
	private double valor;
	
	public ObjetoValor(double peso, double valor) {
		this.peso = peso;
		this.valor = valor;
	}

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public double getValor() {
		return valor;
	}

	public void setValor(double valor) {
		this.valor = valor;
	}
	
	
}
